package shardsetup

import (
	"fmt"
	"os"
	"path/filepath"
	"testing"

	"github.com/multigres/multigres/go/provisioner/local"
)

// MultipoolerPGTLSCertPaths holds the paths used to provision postgres with TLS
// and to point the multipooler at the same CA when verifying the server cert.
type MultipoolerPGTLSCertPaths struct {
	CACertFile     string
	ServerCertFile string
	ServerKeyFile  string

	// ExtraConfFile is a postgresql.conf snippet that turns on SSL and points
	// at the generated server certificate. pgctld appends it via
	// --pg-initdb-extra-conf so postgres boots with TLS enabled.
	ExtraConfFile string

	// HbaTemplateFile is a pg_hba.conf template that trusts 127.0.0.1 over
	// (host)ssl so the multipooler's per-user pools can authenticate with the
	// empty-password fallback path while we verify the TLS dial works
	// end-to-end. The default template requires SCRAM, which would force the
	// test to plumb passthrough keys through gRPC just to assert TLS.
	HbaTemplateFile string
}

// pgHbaTrustTemplate matches the default template's other entries but trusts
// localhost connections so the multipooler's regular pools — which dial with
// password="" unless SCRAM passthrough keys are set — can connect over TLS
// without involving the multigateway.
const pgHbaTrustTemplate = `# Generated by shardsetup.WithMultipoolerPGTLS
# TYPE  DATABASE        USER            ADDRESS                 METHOD

local   all             {{.User}}       trust
local   all             all             trust

local   replication     all                                     trust
host    replication     all             127.0.0.1/32            trust
host    replication     all             ::1/128                 trust

host    all             all             127.0.0.1/32            trust
host    all             all             ::1/128                 trust
`

// generateMultipoolerPGTLSAssets builds the CA + server cert and writes the
// postgres SSL config snippet that pgctld will splice into postgresql.conf.
//
// One CA is shared by every pooler in the setup so any pgctld can verify the
// others, and the multipooler's --pg-client-sslrootcert can point at a single
// file. The server cert binds CN=localhost / SAN=localhost — multipooler dials
// the colocated postgres over TCP using its own hostname, which shardsetup sets
// to "localhost".
//
// pg_hba.conf is left untouched; the default template already accepts TLS
// connections from 127.0.0.1 with scram-sha-256 (the `host` rule applies to
// both ssl and non-ssl), and the admin password is provisioned through
// POSTGRES_PASSWORD on every multipooler/pgctld process.
func (s *ShardSetup) generateMultipoolerPGTLSAssets(t *testing.T) {
	t.Helper()

	certDir := filepath.Join(s.TempDir, "multipooler-pgtls")
	if err := os.MkdirAll(certDir, 0o755); err != nil {
		t.Fatalf("failed to create multipooler PG TLS dir: %v", err)
	}

	caCertFile := filepath.Join(certDir, "ca.crt")
	caKeyFile := filepath.Join(certDir, "ca.key")
	if err := local.GenerateCA(caCertFile, caKeyFile); err != nil {
		t.Fatalf("failed to generate CA for multipooler PG TLS: %v", err)
	}

	serverCert := filepath.Join(certDir, "server.crt")
	serverKey := filepath.Join(certDir, "server.key")
	if err := local.GenerateCert(caCertFile, caKeyFile, serverCert, serverKey, "localhost", []string{"localhost"}); err != nil {
		t.Fatalf("failed to generate server cert for multipooler PG TLS: %v", err)
	}

	// Postgres requires the server key to be 0600 owned by the postgres user.
	// In tests the running user is also the postgres user, so chmod is enough.
	if err := os.Chmod(serverKey, 0o600); err != nil {
		t.Fatalf("failed to chmod server key: %v", err)
	}

	extraConf := filepath.Join(certDir, "ssl_extra.conf")
	contents := fmt.Sprintf(`# Generated by shardsetup.WithMultipoolerPGTLS for the MUL-370 integration test.
ssl = on
ssl_cert_file = '%s'
ssl_key_file  = '%s'
ssl_ca_file   = '%s'
`, serverCert, serverKey, caCertFile)
	if err := os.WriteFile(extraConf, []byte(contents), 0o600); err != nil {
		t.Fatalf("failed to write SSL extra conf: %v", err)
	}

	hbaTmpl := filepath.Join(certDir, "pg_hba_trust.conf")
	if err := os.WriteFile(hbaTmpl, []byte(pgHbaTrustTemplate), 0o600); err != nil {
		t.Fatalf("failed to write pg_hba template: %v", err)
	}

	s.MultipoolerPGTLSCertPaths = &MultipoolerPGTLSCertPaths{
		CACertFile:      caCertFile,
		ServerCertFile:  serverCert,
		ServerKeyFile:   serverKey,
		ExtraConfFile:   extraConf,
		HbaTemplateFile: hbaTmpl,
	}

	t.Logf("Generated multipooler PG TLS assets in %s", certDir)
}
